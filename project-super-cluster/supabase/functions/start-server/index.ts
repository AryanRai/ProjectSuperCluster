import "jsr:@supabase/functions-js/edge-runtime.d.ts"

import { serve } from 'https://deno.land/std@0.168.0/http/server.ts'
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const supabase = createClient(
  Deno.env.get('SUPABASE_URL') ?? '',
  Deno.env.get('SUPABASE_ANON_KEY') ?? ''
)

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

console.log("Hello from Functions!")

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const startCommand = Deno.env.get('MINECRAFT_SERVER_DOCKER_COMMAND') || 
      'docker run -it --rm -v /home/aryan/Desktop/Minecraft:/minecraft -w /minecraft -p 25565:25565 openjdk:21 java -Xmx2G -Xms2G -jar 1-21-4-fab.jar'
    
    // Insert command into the queue
    const { error } = await supabase
      .from('server_commands')
      .insert({
        command: startCommand,
        type: 'start',
        status: 'pending'
      })

    if (error) throw error

    return new Response(
      JSON.stringify({
        success: true,
        message: 'Start server command queued successfully',
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      },
    )
  } catch (error: any) {
    return new Response(
      JSON.stringify({
        success: false,
        error: error.message,
      }),
      {
        status: 500,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      },
    )
  }
})

